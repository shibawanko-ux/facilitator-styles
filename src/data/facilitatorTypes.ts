// タイプを判定する関数
export function determineType(
  intervention: 'trigger' | 'watch',
  perception: 'observe' | 'insight',
  judgment: 'goal' | 'relation',
  engagement: 'design' | 'improvise'
): FacilitatorType | undefined {
  return facilitatorTypes.find(
    (type) =>
      type.intervention === intervention &&
      type.perception === perception &&
      type.judgment === judgment &&
      type.engagement === engagement
  );
}
